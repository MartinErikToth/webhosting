        INSERT INTO FELHASZNALOK (
            BE_VAN_JELENTKEZVE, EMAIL, FELHASZNALONEV, JELSZO, SZEREP
        ) VALUES (
            'N', p_email, p_fnev, p_jelszo, p_szerep
        ) RETURNING ID INTO p_user_id;
    END IF;
END;

